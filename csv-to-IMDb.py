import pandas as pd
import requests
import time
import random
import urllib.parse
from tqdm import tqdm

# Read your CSV and cookie
df = pd.read_csv("base.csv")
cookie = open("cookie.txt", "r", encoding="latin-1").read().strip()

# Ensure the 'const' column exists
if "const" not in df.columns:
    df.insert(0, "const", "")

headers = {
    "User-Agent": "Mozilla/5.0",
    "Content-Type": "application/json",
    "cookie": cookie
}

# â”€â”€ 2) OMDb API key â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
OMDB_KEY = input("â–¶ Enter your free OMDb API key: ").strip()

# â”€â”€ 3) Function to look up IMDb IDs via OMDb â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
def find_imdb_id(title: str, year: int) -> str | None:
    q = urllib.parse.quote(title)
    # Exact match (title + year)
    url1 = f"https://www.omdbapi.com/?t={q}&y={year}&apikey={OMDB_KEY}"
    r1 = requests.get(url1, timeout=10).json()
    if r1.get("Response") == "True":
        return r1.get("imdbID")
    # Broad search, then filter by year
    url2 = f"https://www.omdbapi.com/?s={q}&apikey={OMDB_KEY}"
    r2 = requests.get(url2, timeout=10).json()
    if r2.get("Response") == "True":
        for hit in r2["Search"]:
            if str(year) in hit.get("Year", ""):
                return hit["imdbID"]
        # Fallback to first result
        return r2["Search"][0]["imdbID"]
    return None

# â”€â”€ 4) Fill in missing IMDb IDs â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
print("\nğŸ” Looking up IMDb IDs via OMDbâ€¦")
found = 0
for i, row in tqdm(df.iterrows(), total=len(df)):
    if isinstance(row["const"], str) and row["const"].startswith("tt"):
        continue
    imdb_id = find_imdb_id(row["Title"], int(row["Year"]))
    if imdb_id:
        df.at[i, "const"] = imdb_id
        found += 1
    else:
        print(f"â“ Not found: {row['Title']} ({row['Year']})")
    time.sleep(random.uniform(0.2, 0.4))
print(f"âœ… Found {found}/{len(df)} IMDb IDs")

# Save intermediate CSV
