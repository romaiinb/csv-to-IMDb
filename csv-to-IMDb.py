# â”€â”€ 5) Function to submit ratings via IMDb GraphQL â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
def rate_imdb(tt: str, rating: int):
    payload = {
        "query": """
            mutation UpdateTitleRating($rating:Int!,$titleId:ID!){
              rateTitle(input:{rating:$rating,titleId:$titleId}){rating{value}}
            }""",
        "operationName": "UpdateTitleRating",
        "variables": {"rating": rating, "titleId": tt}
    }
    response = requests.post(
        "https://api.graphql.imdb.com/",
        headers=headers,
        json=payload,
        timeout=10
    )
    if response.status_code == 429:
        raise RuntimeError("IMDb rate-limited; please wait a bitâ€¦")
    if response.status_code != 200:
        raise RuntimeError(f"HTTP {response.status_code}: {response.text[:100]}")
    if response.json().get("errors"):
        raise RuntimeError(response.json()["errors"][0]["message"])

# â”€â”€ 6) Upload all ratings â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
print("\nâ­ Uploading ratings to IMDbâ€¦")
ok, ko = 0, 0
for _, row in tqdm(df.iterrows(), total=len(df)):
    tt, rating = row["const"], int(row["your rating"])
    if not str(tt).startswith("tt"):
        ko += 1
        continue
    try:
        rate_imdb(tt, rating)
        ok += 1
        time.sleep(random.uniform(0.3, 0.6))
    except Exception as e:
        print(f"âš ï¸ {tt} â€“ {e}")
        ko += 1

print(f"\nğŸ‰ Done: {ok} successes, {ko} failures")